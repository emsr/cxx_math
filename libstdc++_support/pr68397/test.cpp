#include <iostream>
#include <stdexcept>
#include <limits>
#include <cmath>

int
main()
{
  std::cout.precision(std::numeric_limits<long double>::max_digits10);
  for(long double x = -1.600001L; x < -0.9L; x += 0.05L)
  {
    try
    {
      std::cout << "std::expint(" << x << ")= " << std::expint(x) << std::endl;
    }
    catch (std::exception& e)
    {
      std::cerr << "std::expint(" << x << ") failed with error: " << e.what() << std::endl;
    }
  }
  return 0;
}
